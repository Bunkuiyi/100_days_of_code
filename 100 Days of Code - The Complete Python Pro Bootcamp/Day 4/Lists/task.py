# Data structure - way of organising and storing data
# Variable stores one piece of data
# Lists are a way to store multiple types of related data (they have an order)

states_of_america = ["Delaware", "Illinois", "Arizona"]

states_of_america[-2] = "Chicago"

states_of_america.append("London")

states_of_america.extend(["Paris", "Tokyo"])

print(states_of_america)
